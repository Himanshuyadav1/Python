# Data Types in Python
""""
    1. Integer
    2. Float
    3. Complex
    4. String
    5. Boolean
    6. List
    7. Tuple
    8. Sets
    9. Dictionary    
"""

# Integer
print(7)
# check type
print(type(7), end="\n\n")

# Float
print(2.5)
# check type
print(type(2.5), end="\n\n")

# Complex
print(2+5j)
# check type
print(type(2+5j), end="\n\n")

# String
print("Python")
# check type
print(type("Python"), end="\n\n")

# Boolen
print(True)
print(False)
# check type
print(type(True))
print(type(False), end="\n\n")

# List
print([1, "Python", True])
# check type
print(type([1, "Python", True]), end="\n\n")

# Tuple
print((1, "Python", True))
# check type
print(type((1, "Python", True)), end="\n\n")

# Sets
print({1, "Python", True})
# check type
print(type({1, "Python", True}), end="\n\n")
# Why sets remove boolean?

# Dictionary
print({"language_name": "Python", "version": "3.13.4"})
# check type
print(type({"language_name": "Python", "version": "3.13.4"}), end="\n\n")