# Identifiers

# Rules for defining identifiers

# 1. You can't start with a digit
# 1name = "Python" # will show error
name1 = "Python" # will work

# 2. You can't use any special chars expect underscore (_)
# *name = "Python" # will show error
# name* = "Python" # will show error
# name@ = "Python" # will show error
_name = "Python" # will work
name_ = "Python" # will work
_ = "Python" # will work