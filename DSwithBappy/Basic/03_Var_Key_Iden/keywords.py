# Keywords

"""
Key Points:
    Python keywords are case-sensitive.
    The number of keywords can vary slightly between Python versions. As of Python 3.11, there are 35 keywords.
    Keywords cannot be used as variable names or other identifiers. 
    Using a keyword as an identifier will result in a syntax error. 

Categories of Keywords:
    Value Keywords: True, False, None
    Operator Keywords: and, or, not, in, is
    Conditional Keywords: if, elif, else
    Iteration Keywords: for, while, break, continue, pass
    Structure Keywords: def, class, return, lambda, with, as
    Import Keywords: import, from
    Exception Handling Keywords: try, except, raise, finally, assert
    Asynchronous Programming Keywords: async, await
    Variable Handling Keywords: del, global, nonlocal
    
Examples of Keywords:
    True, False: Boolean values.
    and, or, not: Logical operators.
    if, elif, else: Conditional statements.
    for, while: Loop statements.
    def: Defines a function.
    class: Defines a class.
    import: Imports modules.
    try, except: Exception handling.
    lambda: Creates anonymous functions.
    return: Returns a value from a function.
    yield: Returns a generator.
"""